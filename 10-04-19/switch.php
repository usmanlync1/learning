<?php

// Condition Statements
// There are 3 types of conditions:
// 1) If/else
// 2)Switch
// 3)itinerary operator

// Syntax of SWITCH
$value = 0;
switch ($value) {
  case '9':
    echo "value is 9";
    break;

  case '3':
    echo "value is 3";
    break;

  default:
    echo "No value!";
    break;
}

 ?>
